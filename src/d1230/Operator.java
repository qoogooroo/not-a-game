package d1230;

public class Operator {
	public static void main(String[] args) {
		int num1 = 10, num2 =3;
		int result = num1 + num2;
		System.out.println(result);
		
		result = num1 - num2;
		System.out.println(result);
		
		result = num1 * num2;
		System.out.println(result);
		
		result = num1 / num2;
		System.out.println(result);
		
		double result1 = num1 / (double)num2;
		System.out.println(result1);
		
		result = num1 % num2;
		System.out.println(result);
	}
}
